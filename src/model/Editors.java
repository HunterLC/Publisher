package model;

public class Editors {
	private int ID;
	private String editorName;
	private int editorBjsID;
	public int getID() {
		return ID;
	}
	public void setID(int iD) {
		ID = iD;
	}
	public String getEditorName() {
		return editorName;
	}
	public void setEditorName(String editorName) {
		this.editorName = editorName;
	}
	public int getEditorBjsID() {
		return editorBjsID;
	}
	public void setEditorBjsID(int editorBjsID) {
		this.editorBjsID = editorBjsID;
	}
}
